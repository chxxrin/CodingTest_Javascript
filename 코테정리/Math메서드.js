// Math.round: 반올림
console.log(Math.round(4.6)); // 5
console.log(Math.round(4.4)); // 4

// Math.ceil: 올림
console.log(Math.ceil(4.2)); // 5
console.log(Math.ceil(4.9)); // 5

// Math.floor: 내림
console.log(Math.floor(4.9)); // 4
console.log(Math.floor(4.2)); // 4

// Math.abs: 절대값
console.log(Math.abs(-5)); // 5
console.log(Math.abs(5)); // 5